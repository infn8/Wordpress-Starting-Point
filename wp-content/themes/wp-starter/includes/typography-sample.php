	<h3 class="short_headline"><span>Phrase Elements</span></h3>
	<h5>Em</h5>
	<p> Element used to <em>put emphasis on certain information</em>. Most browsers display emphased text in italics by default. </p>
	<p> Sample: <em>This is emphasied text</em>. </p>
	<h5>Strong</h5>
	<p> This element stands for "stronger emphasis" and is used for marking more important text. </p>
	<p> Sample: <strong>This is text with stronger emphasis</strong> </p>
	<h5>Cite</h5>
	<p> According to <cite>W3C specification</cite>, we use <code>cite</code> element to define source of a quotation or reference. </p>
	<p> Sample: <cite>This is quotation source</cite> </p>
	<h4>Dfn</h4>
	<p> Element used to markup inline definition of a single term. </p>
	<p> Sample: <dfn>This is definition</dfn> </p>
	<h5>code</h5>
	<p> This element informs the browser that it contains a computer code, such as XHTML markup. </p>
	<p> Sample: <code>document.write("Hello world");</code> </p>
	<h4>samp</h4>
	<p> Defines computer output data, for example we can use it to markup error messages </p>
	<p> Sample: <samp>Error: no such file or directory</samp> </p>
	<h5>kbd</h5>
	<p> Means information that should be entered by the user. </p>
	<p> Sample: press <kbd>Alt + F4</kbd> to close this window. <kbd>ESC</kbd> </p>
	<h4>var</h4>
	<p> Means variables used in computer programs or scripts. </p>
	<p> Sample: <var>counter</var> </p>
	<h5>abbr</h5>
	<p> Abbreviation / a shortened form of a word or phrase </p>
	<p> Sample: <abbr title="doctor">dr</abbr> </p>
	<h5>q: inline quotation</h5>
	<p>Sample: <q>This is a sample inline quotation</q></p>
